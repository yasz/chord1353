async function f1(keyword) {
    let song = ""
    try {
        const res = await fetch(`https://www.zanmeishi.com/search/autosuggest/?k=${keyword}`, {
            method: 'GET'
        });

        if (res.ok) {
            const data = await res.text();
            // debugger
            let output = await JSON.parse(data);
            if (output["song"]) {
                //async 做异步循环的时候最好用 for ... of ... 或者 Promise.all()
                // output["song"].forEach((item, index) => { //
                for (let elem of output["song"]) {
                    song += await f2(elem["id"])+"\n" //循环遍历，直到不为空的结果出
                }
            }
        }
    } catch (ex) {
        console.log('err +++++', ex);
    }
    // console.log("return " + song)
    return song
};
async function f2(id) {
    const res = await fetch(`https://api.zanmeishi.com/song/text?song_id=${id}`, {
        method: 'GET'
    });
    if (res.ok) {
        const data = await res.text();
        return JSON.parse(data)["lyric"]
    }
}

